package info.project.datapotal.viewpager.sign.vo;

public class DrawMenu {
	
	private int image;
	private String menu;
	
	public DrawMenu(int image, String menu)
	{
		this.image = image;
		this.menu = menu;
	}
	
	public int getImage()
	{
		return image;
	}
	
	public String getMenu()
	{
		return menu;
	}

}
